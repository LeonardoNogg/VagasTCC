package com.example.nogg.vagastcc.Entidades;

public class Solicitacoes {

    private String id;
    private String id_aluno;
    private String nome_aluno;
    private String email_aluno;
    private String id_vaga;
    private String empresa_vaga;
    private String tipo_vaga;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId_aluno() {
        return id_aluno;
    }

    public void setId_aluno(String id_aluno) {
        this.id_aluno = id_aluno;
    }

    public String getNome_aluno() {
        return nome_aluno;
    }

    public void setNome_aluno(String nome_aluno) {
        this.nome_aluno = nome_aluno;
    }

    public String getEmail_aluno() {
        return email_aluno;
    }

    public void setEmail_aluno(String email_aluno) {
        this.email_aluno = email_aluno;
    }

    public String getId_vaga() {
        return id_vaga;
    }

    public void setId_vaga(String id_vaga) {
        this.id_vaga = id_vaga;
    }

    public String getEmpresa_vaga() {
        return empresa_vaga;
    }

    public void setEmpresa_vaga(String empresa_vaga) {
        this.empresa_vaga = empresa_vaga;
    }

    public String getTipo_vaga() {
        return tipo_vaga;
    }

    public void setTipo_vaga(String tipo_vaga) {
        this.tipo_vaga = tipo_vaga;
    }

    public Solicitacoes() {
    }
}
